<div id="ta_3" class="t s8_3">§ 40-6-163. Duty of driver of vehicle meeting or overtaking school bus; reporting of violations; </div>
<div id="tb_3" class="t s8_3">enforcement</div>
<div id="tc_3" class="t s8_3">(a) Except as provided in subsection (b) of this Code section, the driver of a vehicle meeting or </div>
<div id="td_3" class="t s8_3">overtaking from either direction any school bus stopped on the highway shall stop before </div>
<div id="te_3" class="t s8_3">reaching such school bus when there are in operation on the school bus the visual signals as </div>
<div id="tf_3" class="t s8_3">specified in Code Sections 40-8-111 and 40-8-115, and such driver shall not proceed until the </div>
<div id="tg_3" class="t s8_3">school bus resumes motion or the visual signals are no longer actuated.</div>
<div id="th_3" class="t s8_3">(b) The driver of a vehicle upon a highway with separate roadways need not stop upon meeting or </div>
<div id="ti_3" class="t s8_3">passing a school bus which is on a different roadway, or upon a controlled-access highway when </div>
<div id="tj_3" class="t s8_3">the school bus is stopped in a loading zone which is a part of or adjacent to such highway and </div>
<div id="tk_3" class="t s8_3">where pedestrians are not permitted to cross the roadway.</div>
<div id="tl_3" class="t s8_3">(c) Every school bus driver who observes a violation of subsection (a) of this Code section is </div>
<div id="tm_3" class="t s8_3">authorized and directed to record specifically the vehicle description, license number of the </div>
<div id="tn_3" class="t s8_3">offending vehicle, and time and place of occurrence on forms furnished by the Department of </div>
<div id="to_3" class="t s8_3">Public Safety. Such report shall be submitted within 15 days of the occurrence of the violation to </div>
<div id="tp_3" class="t s8_3">the local law enforcement agency which has law enforcement jurisdiction where the alleged </div>
<div id="tq_3" class="t s8_3">offense occurred.</div>
<div id="tr_3" class="t s8_3">(d) (1) As used in this subsection, the term:</div>
<div id="ts_3" class="t s8_3">(A) "Owner" means the registrant of a motor vehicle, except that such term shall not include a </div>
<div id="tt_3" class="t s8_3">motor vehicle rental company when a motor vehicle registered by such company is being </div>
<div id="tu_3" class="t s8_3">operated by another person under a rental agreement with such company.</div>
<div id="tv_3" class="t s8_3">(B) "Recorded images" means images recorded by a video recording device mounted on a </div>
<div id="tw_3" class="t s8_3">school bus with a clear view of vehicles passing the bus on either side and showing the date and </div>
<div id="tx_3" class="t s8_3">time the recording was made and an electronic symbol showing the activation of amber lights, </div>
<div id="ty_3" class="t s8_3">flashing red lights, stop arms, and brakes.</div>
<div id="tz_3" class="t s8_3">(C) "Video recording device" means a camera capable of recording digital images showing the </div>
<div id="t10_3" class="t s8_3">date and time of the images so recorded.</div>
<div id="t11_3" class="t s8_3">(2) Subsection (a) of this Code section may be enforced by using recorded images as provided in </div>
<div id="t12_3" class="t s8_3">this subsection.</div>
<div id="t13_3" class="t s8_3">(3) For the purpose of enforcement pursuant to this subsection:</div>
<div id="t14_3" class="t s8_3">(A) The driver of a motor vehicle shall be liable for a civil monetary penalty if such vehicle is </div>
<div id="t15_3" class="t s8_3">found, as evidenced by recorded images, to have been operated in disregard or disobedience of </div>
<div id="t16_3" class="t s8_3">subsection (a) of this Code section and such disregard or disobedience was not otherwise </div>
<div id="t17_3" class="t s8_3">authorized by law. The amount of such fine shall be $300.00 for a first offense, $750.00 for a </div>
<div id="t18_3" class="t s8_3">second offense, and $1,000.00 for each subsequent offense in a five-year period;</div>
<div id="t19_3" class="t s8_3">(B) The law enforcement agency authorized to enforce the provisions of this Code section shall </div>
<div id="t1a_3" class="t s8_3">send by regular mail addressed to the owner of the motor vehicle postmarked not later than ten </div>
<div id="t1b_3" class="t s8_3">days after the date of the alleged violation:</div>
<div id="t1c_3" class="t s8_3">(i) A citation for the alleged violation, which shall include the date and time of the violation, </div>
<div id="t1d_3" class="t s8_3">the location of the infraction, the amount of the civil monetary penalty imposed, and the date by </div>
<div id="t1e_3" class="t s8_3">which the civil monetary penalty shall be paid;</div>
<div id="t1f_3" class="t s8_3">(ii) An image taken from the recorded image showing the vehicle involved in the infraction;</div>
<div id="t1g_3" class="t s8_3">(iii) A copy of a certificate sworn to or affirmed by a certified peace officer employed by a law </div>
<div id="t1h_3" class="t s8_3">enforcement agency authorized to enforce this Code section and stating that, based upon </div>
<div id="t1i_3" class="t s8_3">inspection of recorded images, the owner's motor vehicle was operated in disregard or </div>
<div id="t1j_3" class="t s8_3">disobedience of subsection (a) of this Code section and that such disregard or disobedience was </div>
<div id="t1k_3" class="t s8_3">not otherwise authorized by law;</div>
<div id="t1l_3" class="t s8_3">(iv) A statement of the inference provided by subparagraph (D) of this paragraph and of the </div>
<div id="t1m_3" class="t s8_3">means specified therein by which such inference may be rebutted;</div>
<div id="t1n_3" class="t s8_3">(v) Information advising the owner of the motor vehicle of the manner and time in which </div>
<div id="t1o_3" class="t s8_3">liability as alleged in the citation may be contested in court; and</div>
<div id="t1p_3" class="t s8_3">(vi) A warning that failure to pay the civil monetary penalty or to contest liability in a timely </div>
<div id="t1q_3" class="t s8_3">manner shall waive any right to contest liability and result in a civil monetary penalty;</div>
<div id="t1r_3" class="t s8_3">(C) Proof that a motor vehicle was operated in disregard or disobedience of subsection (a) of </div>
<div id="t1s_3" class="t s8_3">this Code section shall be evidenced by recorded images. A copy of a certificate sworn to or </div>
<div id="t1t_3" class="t s8_3">affirmed by a certified peace officer employed by a law enforcement agency and stating that, </div>
<div id="t1u_3" class="t s8_3">based upon inspection of recorded images, a motor vehicle was operated in disregard or </div>
<div id="t1v_3" class="t s8_3">disobedience of subsection (a) of this Code section and that such disregard or disobedience was </div>
<div id="t1w_3" class="t s8_3">not otherwise authorized by law shall be prima-facie evidence of the facts contained therein; and</div>
<div id="t1x_3" class="t s8_3">(D) Liability under this subsection shall be determined based upon preponderance of the </div>
<div id="t1y_3" class="t s8_3">evidence. Prima-facie evidence that the vehicle described in the citation issued pursuant to this </div>
<div id="t1z_3" class="t s8_3">subsection was operated in violation of subsection (a) of this Code section, together with proof </div>
<div id="t20_3" class="t s8_3">that the defendant was at the time of such violation the registered owner of the vehicle, shall </div>
<div id="t21_3" class="t s8_3">permit the trier of fact in its discretion to infer that such owner of the vehicle was the driver of </div>
<div id="t22_3" class="t s8_3">the vehicle at the time of the alleged violation. Such an inference may be rebutted if the owner </div>
<div id="t23_3" class="t s8_3">of the vehicle:</div>
<div id="t24_3" class="t s8_3">(i) Testifies under oath in open court or submits to the court a sworn notarized statement </div>
<div id="t25_3" class="t s8_3">that he or she was not the operator of the vehicle at the time of the alleged violation and </div>
<div id="t26_3" class="t s8_3">identifies the name of the operator of the vehicle at the time of the alleged violation; or</div>
<div id="t27_3" class="t s8_3">(ii) Presents to the court a certified copy of a police report showing that the vehicle had </div>
<div id="t28_3" class="t s8_3">been reported to the police as stolen prior to the time of the alleged violation.</div>
<div id="t29_3" class="t s8_3">(4) A violation for which a civil penalty is imposed pursuant to this subsection shall not be </div>
<div id="t2a_3" class="t s8_3">considered a moving traffic violation for the purpose of points assessment under Code Section </div>
<div id="t2b_3" class="t s8_3">40-5-57. Such violation shall be deemed noncriminal, and imposition of a civil penalty pursuant to </div>
<div id="t2c_3" class="t s8_3">this subsection shall not be deemed a conviction and shall not be made a part of the operating </div>
<div id="t2d_3" class="t s8_3">record of the person upon whom such liability is imposed, nor shall it be used for any insurance </div>
<div id="t2e_3" class="t s8_3">purposes in the provision of motor vehicle insurance coverage.</div>
<div id="t2f_3" class="t s8_3">(5) If a person summoned by regular mail fails to appear on the date of return set out in the </div>
<div id="t2g_3" class="t s8_3">citation and has not paid the penalty for the violation or filed a police report or notarized </div>
<div id="t2h_3" class="t s8_3">statement pursuant to subparagraph (D) of paragraph (3) of this subsection, the person shall </div>
<div id="t2i_3" class="t s8_3">then be summoned a second time by certified mail with a return receipt requested. The second </div>
<div id="t2j_3" class="t s8_3">summons shall include all information required in subparagraph (B) of paragraph (3) of this </div>
<div id="t2k_3" class="t s8_3">subsection for the initial summons and shall include a new date of return. If a person summoned </div>
<div id="t2l_3" class="t s8_3">by certified mail again fails to appear on the date of return set out in the second citation and has </div>
<div id="t2m_3" class="t s8_3">failed to pay the penalty or file an appropriate document for rebuttal, the person summoned </div>
<div id="t2n_3" class="t s8_3">shall have waived the right to contest the violation and shall be liable for the civil monetary </div>
<div id="t2o_3" class="t s8_3">penalty provided in paragraph (3) of this subsection.</div>
<div id="t2p_3" class="t s8_3">(6) Any court having jurisdiction over violations of subsection (a) of this Code section shall have </div>
<div id="t2q_3" class="t s8_3">jurisdiction over cases arising under this subsection and shall be authorized to impose the civil </div>
<div id="t2r_3" class="t s8_3">monetary penalty provided by this subsection. Except as otherwise provided in this subsection, </div>
<div id="t2s_3" class="t s8_3">the provisions of law governing jurisdiction, procedure, defenses, adjudication, appeal, and </div>
<div id="t2t_3" class="t s8_3">payment and distribution of penalties otherwise applicable to violations of subsection (a) of this </div>
<div id="t2u_3" class="t s8_3">Code section shall apply to enforcement under this subsection except as provided in </div>
<div id="t2v_3" class="t s8_3">subparagraph (A) of paragraph (3) of this subsection; provided, however, that any appeal from </div>
<div id="t2w_3" class="t s8_3">superior or state court shall be by application in the same manner as that provided by Code </div>
<div id="t2x_3" class="t s8_3">Section 5-6-35.</div>
<div id="t2y_3" class="t s8_3">(7) Recorded images made for purposes of this subsection shall not be a public record for </div>
<div id="t2z_3" class="t s8_3">purposes of Article 4 of Chapter 18 of Title 50.</div>
<div id="t30_3" class="t s8_3">(8) A governing authority shall not impose a civil penalty under this subsection on the owner of </div>
<div id="t31_3" class="t s8_3">a motor vehicle if the operator of the vehicle was arrested or issued a citation and notice to </div>
<div id="t32_3" class="t s8_3">appear by a peace officer for the same violation.</div>
<div id="t33_3" class="t s8_3">(9) A school system may enter into an intergovernmental agreement with a local governing </div>
<div id="t34_3" class="t s8_3">authority to offset expenses regarding the implementation and ongoing operation of video </div>
<div id="t35_3" class="t s8_3">recording devices serving the purpose of capturing recorded images of motor vehicles unlawfully </div>
<div id="t36_3" class="t s8_3">passing a school bus.</div>
<div id="t37_3" class="t s8_3">(10) Any school bus driver operating a vehicle equipped with an activated video recording </div>
<div id="t38_3" class="t s8_3">device shall be exempt from the recording provisions of subsection (c) of Code Section 40-6-163.</div>
